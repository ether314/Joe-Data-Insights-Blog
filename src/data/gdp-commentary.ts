export function formatYearCommentary(c: YearCommentary): string {
  return `China — ${c.china}\n\nUnited States — ${c.us}\n\nIndia — ${c.india}`;
}

export function gapChartNote(year: number, compare: "us" | "india"): string {
  const c = COUNTRY_COMMENTARY[year];
  if (compare === "us") {
    return `China — ${c.china}\n\nUnited States — ${c.us}`;
  }
  return `China — ${c.china}\n\nIndia — ${c.india}`;
}

